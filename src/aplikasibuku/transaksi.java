    private void bBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBayarActionPerformed
        try{
            if (tOrang.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Nama Wajib diisi");
                tOrang.requestFocus();
            }else if(tUang.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Masukan Nominal uang");
                tUang.requestFocus();
            }else{
                int a,b,c;
        
                a = Integer.parseInt(tHargaTotal.getText());
                b = Integer.parseInt(tUang.getText());
                c = b-a;
                if (b < a) {
                    JOptionPane.showMessageDialog(rootPane, "Uang tidak cukup");
                    tUang.setText("");
                }else{
                   tKembalian.setText(String.valueOf(c));
                    JOptionPane.showMessageDialog(rootPane, "Berhasil,silahkan klik tombol Tambah");
                    tampilData();
                    bTambah.setEnabled(true);
                }       
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_bBayarActionPerformed

