    private void bCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCariActionPerformed
        try {
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("select*from buku where " + "kode_buku='" + tKode.getText() + "'");
            if(rs.next()){
                tKode.setText(rs.getString("kode_buku"));
                tNama.setText(rs.getString("nama_buku"));
                tPengarang.setText(rs.getString("pengarang"));
                tPenerbit.setText(rs.getString("penerbit"));
                tHarga.setText(rs.getString("harga"));
                bTambah.setEnabled(false);
                tJumlah.setEnabled(true);
                tTotal.setEnabled(false);
                bTransaksi.setEnabled(true);
                tOrang.setEnabled(false);
                cmb1.setEnabled(false);
                tHargaTotal.setEnabled(false);
                tUang.setEnabled(false);
                bBayar.setEnabled(false);
                tKembalian.setEnabled(false);
            }else{
                tampilData();
                JOptionPane.showMessageDialog(rootPane, "Kode Buku tidak ditemukan!");
                reset();
                tKode.requestFocus();
            }tampilData();
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_bCariActionPerformed

